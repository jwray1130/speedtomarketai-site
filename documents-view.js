  // trap Tab cycles within the modal. Restores prior focus on close so
  // keyboard users return to where they were. Tab/Shift+Tab cycles inside
  // the modal — Escape closes (handled by existing keydown listener).
  const _focusTraps = new Map();
  function _focusables(root) {
    return Array.from(root.querySelectorAll(
      'a[href], button:not([disabled]):not([style*="display:none"]):not([style*="display: none"]), input:not([disabled]), select:not([disabled]), textarea:not([disabled]), [tabindex]:not([tabindex="-1"]), [contenteditable="true"]'
    )).filter(el => el.offsetWidth > 0 || el.offsetHeight > 0);
  }
  function trapFocus(modalEl) {
    if (!modalEl || _focusTraps.has(modalEl)) return;
    const focusables = _focusables(modalEl);
    if (focusables.length === 0) return;
    const first = focusables[0];
    const last  = focusables[focusables.length - 1];
    const previouslyFocused = document.activeElement;
    const onKeyDown = (e) => {
      if (e.key !== 'Tab') return;
      // Re-snapshot focusables since modal contents can change (color
      // swatches reveal, etc.) — using the original list might trap users.
      const live = _focusables(modalEl);
      if (live.length === 0) return;
      const liveFirst = live[0];
      const liveLast  = live[live.length - 1];
      if (e.shiftKey && document.activeElement === liveFirst) {
        e.preventDefault();
        liveLast.focus();
      } else if (!e.shiftKey && document.activeElement === liveLast) {
        e.preventDefault();
        liveFirst.focus();
      }
    };
    modalEl.addEventListener('keydown', onKeyDown);
    _focusTraps.set(modalEl, { onKeyDown, previouslyFocused });
    // Defer focus until the modal's transition lets the element accept it.
    setTimeout(() => { try { first.focus(); } catch(e) {} }, 50);
  }
  function releaseFocus(modalEl) {
    const trap = _focusTraps.get(modalEl);
    if (!trap) return;
    modalEl.removeEventListener('keydown', trap.onKeyDown);
    if (trap.previouslyFocused && typeof trap.previouslyFocused.focus === 'function') {
      try { trap.previouslyFocused.focus(); } catch(e) {}
    }
    _focusTraps.delete(modalEl);
  }

