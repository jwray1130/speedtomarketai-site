  // CONTRACT NOTES:
  //   • setSubmissionContext(id?, title?)  — id null/undefined clears scope
  //   • clearSubmissionFilter()             — drops UI filter, keeps activeId
  //   • getDocs()                           — sanitized snapshot, not live
  //   • addDocFromPipeline(payload)         — payload.name required; returns
  //                                           doc.id or null on bad input
  //   • pruneSubmission(id)                 — id required; returns count
  //                                           dropped (0 if id missing)
  window.docsView = {
    setSubmissionContext: (submissionId, submissionTitle) => {
      // Coerce to safe types. Anything truthy non-string gets stringified;
      // null/undefined/'' all clear the scope cleanly.
      const sid   = submissionId ? String(submissionId) : null;
      const title = submissionTitle ? String(submissionTitle).slice(0, 200) : null;
      return setSubmissionContext(sid, title);
    },
    clearSubmissionFilter,
    // v8.5: public refresh API. Idempotent — safe to call repeatedly.
    // Concurrent calls return the in-flight promise instead of starting
    // a second fetch. Use opts.reason for telemetry, opts.submissionId
    // for context tagging in logs.
    // v8.5.3 Issue #7: refreshFromCloud now re-throws on failure so
    // try/catch around the public API works. Internally hydrateFromCloud
    // also stores the error in state._lastHydrateError for the UI's
    // sync-paused render — this addition is purely so programmatic
    // callers can react to failures.
    refreshFromCloud: async (opts) => {
      await hydrateFromCloud(opts || { reason: 'public_api' });
      // After hydrate completes, surface any structured error so the
      // caller doesn't have to also poll getHydrateState() to find out.
      if (state._lastHydrateError) {
        const e = state._lastHydrateError;
        const err = new Error(typeof e === 'string' ? e : (e.message || 'hydrate failed'));
        if (typeof e === 'object') {
          err.supabaseCode = e.code || null;
          err.supabaseDetails = e.details || null;
          err.supabaseHint = e.hint || null;
        }
        throw err;
      }
    },
    // v8.5: read hydrate state for diagnostics. Returns null fields if
    // no hydrate has run yet.
    getHydrateState: () => ({
      hydratedOnce: state._hydratedOnce,
      hydrating: state._hydrating,
      lastHydratedAt: state._lastHydratedAt,
      lastError: state._lastHydrateError,
    }),
    // v8.5.3 Issue #6: toggle verbose telemetry. Call _setVerbose(true) in
    // the console to see hydrate/enrichment logs. Defaults off so the
    // console isn't noisy in production.
    _setVerbose: (on) => { _verbose = !!on; return _verbose; },
    // Snapshot of current docs (sanitized for external read; internal
    // mutable arrays not exposed). v8.5 adds pipelineTag, primaryBucket,
    // pageNumber, totalPages so debug probes can verify field mapping.
    getDocs: () => state.docs.map(d => ({
      id: d.id,
      name: d.displayName,
      category: d.category,
      submissionId: d.submissionId,
      tagged: d.tagged,
      color: d.color,
      pipelineClassification: d.pipelineClassification,
      pipelineTag: d.pipelineTag,
      primaryBucket: d.primaryBucket,
      pageNumber: d.pageNumber,
      totalPages: d.totalPages,
      storagePath: d.storagePath,
    })),
    // Programmatic doc insertion. Lets Altitude's pipeline push a
    // classified doc into the view without going through the upload
    // flow. Caller passes a payload that addDoc consumes.
    // Returns the new doc.id, or null if the payload is unusable.
    addDocFromPipeline: (payload) => {
      if (!payload || typeof payload !== 'object') {
        console.warn('addDocFromPipeline: payload must be an object');
        return null;
      }
      if (!payload.name || typeof payload.name !== 'string') {
        console.warn('addDocFromPipeline: payload.name (string) is required');
        return null;
      }
      const doc = addDoc(payload);
      renderCategoryGrid();
      renderDocsList();
      renderTagsList();
      return doc.id;
    },
    // Pipeline-driven full-fidelity ingestion. Unlike addDocFromPipeline
    // (metadata-only push, no thumbnails or storage), this takes the raw
    // File object and runs the same processing pipeline manual upload uses:
    //   1) upload binary to Supabase storage
    //   2) dispatch to type-specific processor (PDF, Excel, Word, image, etc.)
    //   3) processor renders thumbnails + extracts text
    //   4) addDoc() creates one row per page/sheet/slide, all sharing the
    //      same storage path and inheriting the classifier's category/color
    //      via state._pipelineCtx
    // Returns a Promise that resolves when ingestion completes (or rejects
    // on processor error). Used by Altitude's pipeline.js after classifier
    // run to mirror the broker submission file into the Documents tab with
    // working thumbnails and previews.
    processFileFromPipeline: async (file, ctx) => {
      if (!file || typeof file !== 'object') {
        console.warn('processFileFromPipeline: file required (File or Blob-like)');
        return null;
      }
      if (typeof processFile !== 'function') {
        console.warn('processFileFromPipeline: processFile dispatcher missing');
        return null;
      }
      // ── SERIALIZATION CHAIN ──────────────────────────────────────────────
      // Pipeline classification runs files in parallel (Promise.all in
      // pipeline.js). When their classifier promises all settle around the
      // same time, multiple processFileFromPipeline calls would otherwise
      // run concurrently and stomp on each other:
      //
      //   • state._pipelineCtx  (category, color, classification, submissionId)
      //   • state._uploadCtx    (storagePath, fileId, fileSize, fileName)
      //
      // Both are read by addDoc when stamping new pages. If file B's call
      // overwrites the ctx mid-loop while file A's processPDF is still
      // pumping pages 21..99, those pages end up inheriting B's storagePath
      // and category — cross-contamination that's silent at upload time
      // but breaks previews after refresh because the persisted storage_path
      // points at the wrong binary.
      //
      // UNIFIED CHAIN: we use state._processFileChain so this serializes
      // not just against other pipeline ingestions but ALSO against manual
      // uploads (which feed the same chain via _runUploadBatch). Without
      // that, a user manually uploading mid-pipeline would set
      // state._uploadCtx and corrupt the pipeline file's remaining pages.
      // Errors don't break the chain — caught with .catch(()=>{}) so a single
      // file's failure doesn't poison subsequent ingestions.
      const prev = state._processFileChain || Promise.resolve();
      const next = prev.catch(() => {}).then(async () => {
        // Stamp pipeline context onto state so addDoc inherits it for every
        // page produced by the processor. Cleared in the finally block so it
        // doesn't leak into a subsequent manual upload session.
        state._pipelineCtx = {
          category: (ctx && ctx.category) || 'all',
          color: (ctx && ctx.color) || null,
          pipelineClassification: (ctx && ctx.pipelineClassification) || null,
          pipelineRoutedTo: (ctx && ctx.pipelineRoutedTo) || null,
          // v8.4 fields propagate through to every page addDoc creates.
          // addDoc applies the first-page-only rule on pipelineTag.
          pipelineTag: (ctx && ctx.pipelineTag) || null,
          primaryBucket: (ctx && ctx.primaryBucket) || null,
          submissionId: (ctx && ctx.submissionId) || null,
        };
        const beforeIds = new Set(state.docs.map(d => d.id));
        try {
          // Pass the resolved category to processFile so the per-type
          // processor passes it to addDoc explicitly. addDoc will then
          // accept that category (opts wins over pctx).
          await processFile(file, state._pipelineCtx.category);
        } catch (err) {
          console.warn('processFileFromPipeline: processing failed for ' + file.name + ':', err);
          // Surface to the caller so the pipeline can handle. Don't swallow.
          throw err;
        } finally {
          state._pipelineCtx = null;
          // Note: _uploadCtx is set inside processFile and not cleared
          // automatically. Manual uploads overwrite it on next file; we
          // mirror that here. Clearing explicitly would be cleaner but
          // would diverge from the manual path's semantics.
        }
        const newIds = state.docs.filter(d => !beforeIds.has(d.id)).map(d => d.id);
        // Wait for in-flight Supabase inserts for these new docs to settle
        // before returning. Without this, processFileFromPipeline can
        // resolve while document_pages rows are still being upserted in
        // the background. The pipeline counter would jump to N (matches
        // local state.docs) but a refresh during the gap would hydrate
        // fewer than N rows, looking like a persistence bug. Waiting
        // here makes the contract clear: when this resolves, the rows
        // are persisted (or the per-row error has been logged).
        //
        // .allSettled (not .all) means a single insert failure doesn't
        // reject the whole batch — the per-row catch in addDoc already
        // logged it, and we want to return whatever IDs were created
        // locally regardless of cloud success. Misses (NULL pendingInserts
        // map, doc id not registered) just resolve to undefined which
        // allSettled handles cleanly.
        if (state._pendingInserts && state._pendingInserts.size > 0) {
          const pending = newIds
            .map(id => state._pendingInserts.get(id))
            .filter(Boolean);
          if (pending.length > 0) {
            await Promise.allSettled(pending);
          }
        }

        // BATCH-LEVEL ORPHAN STORAGE CLEANUP
        // After all pending inserts have settled, check whether any
        // binaries we uploaded have zero referencing rows. Definitive
        // existence check at this point — no concurrent inserts in flight.
        // Uses the shared cleanupOrphanStoragePaths helper (also called
        // from _runUploadBatch for the manual upload path).
        const localDocsForBatch = state.docs.filter(d => newIds.includes(d.id));
        const storagePaths = [...new Set(localDocsForBatch.map(d => d.storagePath).filter(Boolean))];
        if (storagePaths.length > 0) {
          // Fire-and-forget — orphan cleanup never blocks the caller.
          cleanupOrphanStoragePaths(storagePaths);
        }

        renderCategoryGrid();
        renderDocsList();
        renderTagsList();
        return newIds;
      });
      state._processFileChain = next;
      return next;
    },
    // Phase 5 — cascade pruning hook. Called by app.js when an Altitude
    // submission is deleted, so the docs view drops the matching local
    // rows immediately (the cloud cascade is a separate fire-and-forget).
    pruneSubmission: (submissionId) => {
      if (!submissionId) return 0;
      const sid = String(submissionId);
      const before = state.docs.length;
      const dropped = state.docs.filter(d => d.submissionId === sid);
      if (dropped.length === 0) return 0;
      revokeDocBlobUrls(dropped);  // also clears annotation store entries + timers
      const droppedIds = new Set(dropped.map(d => d.id));
      state.docs = state.docs.filter(d => !droppedIds.has(d.id));
      // Selection cleanup — drop ids whose docs are gone so the bulk bar
      // doesn't lie about how many things are selected.
      if (state.selectedIds && state.selectedIds.size > 0) {
        droppedIds.forEach(id => state.selectedIds.delete(id));
        if (typeof updateBulkBar === 'function') updateBulkBar();
      }
      // Context-menu target cleanup.
      if (state.contextDoc && droppedIds.has(state.contextDoc)) {
        state.contextDoc = null;
      }
      // If the user is currently scoped to the deleted submission, drop
      // the scope so they aren't stuck looking at an empty view.
      if (state.activeSubmissionId === sid) {
        state.activeSubmissionId = null;
        state.activeSubmissionTitle = null;
        state.submissionFilter = 'all';
        updateSubmissionChip();
      }
      renderCategoryGrid();
      renderDocsList();
      renderTagsList();
      return before - state.docs.length;
    },
  };

