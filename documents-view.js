  // ══════ HTML SANITIZER (defense-in-depth for innerHTML uses) ══════
  // mammoth, EML parsing, PPTX parsing, and HTML text upload all produce
  // HTML strings that we render via innerHTML in thumbnails and previews.
  // Cloud-stored htmlContent comes back through hydrate. While the threat
  // model here is "self-XSS" (a user can't inject content into another
  // user's session because RLS isolates rows), we still strip <script>,
  // on*= handlers, and javascript: URLs so a malicious docx can't run
  // background fetches or modify the docs view's DOM.
  // This is intentionally lightweight (no DOMPurify dependency) — the
  // patterns below cover the common attack surface for HTML pasted from
  // Office files. Anything more sophisticated would need DOMPurify.
  function sanitizeHtml(html) {
    if (typeof html !== 'string' || !html) return html;
    let s = html;
    // Drop <script>...</script> and <iframe>...</iframe> blocks entirely.
    s = s.replace(/<script\b[^>]*>[\s\S]*?<\/script\s*>/gi, '');
    s = s.replace(/<iframe\b[^>]*>[\s\S]*?<\/iframe\s*>/gi, '');
    s = s.replace(/<object\b[^>]*>[\s\S]*?<\/object\s*>/gi, '');
    s = s.replace(/<embed\b[^>]*>/gi, '');
    // Drop event-handler attributes (onclick, onerror, onload, etc.).
    s = s.replace(/\s+on\w+\s*=\s*"[^"]*"/gi, '');
    s = s.replace(/\s+on\w+\s*=\s*'[^']*'/gi, '');
    s = s.replace(/\s+on\w+\s*=\s*[^\s>]+/gi, '');
    // Neutralize javascript:/data:/vbscript: URLs in href/src.
    s = s.replace(/(href|src|action|formaction|background|poster)\s*=\s*"(?:\s*(?:javascript|vbscript|data):[^"]*)"/gi, '$1="#"');
    s = s.replace(/(href|src|action|formaction|background|poster)\s*=\s*'(?:\s*(?:javascript|vbscript|data):[^']*)'/gi, "$1='#'");
    return s;
  }

  function addDoc(opts) {
    const now = Date.now();
    // Pull upload context (storage_path, file_size, etc.) set by processFile
    // earlier in the dispatch. All addDoc calls within one processFile run
    // share the same context because they're slices of the same source file.
    const ctx = state._uploadCtx || {};
    // Pull pipeline context if processFile was invoked from the pipeline
    // (processFileFromPipeline). This carries classifier output so every
    // page produced by the type-specific processor inherits the same
    // category/color/classification metadata. Explicit opts always win
    // (callers can override per-page if needed) — defaults flow from ctx.
    const pctx = state._pipelineCtx || {};
    // Cap display_name at 250 chars. Most filenames are <50 chars; some
    // generated names like 'Long Doc — Page 47' grow but still fit. A
    // pathologically long name (10K chars from a renamed export) would
    // overflow the doc list rows and bloat the DB row size.
    const rawName = (typeof opts.name === 'string') ? opts.name : '';
    const safeName = rawName.length > 250 ? rawName.slice(0, 247) + '…' : rawName;
    const doc = {
      id: 'doc-' + (state.nextId++) + '-' + now,
      name: safeName,
      displayName: safeName,
      type: opts.type || 'unknown',
      category: opts.category || pctx.category || 'all',
      thumbnailData: opts.thumbnailData || null,
      highResData: opts.highResData || null,
      htmlContent: sanitizeHtml(opts.htmlContent) || null,
      textContent: opts.textContent || '',
      pageNumber: opts.pageNumber || 1,
      totalPages: opts.totalPages || 1,
      pdfData: opts.pdfData || null,
      sheetName: opts.sheetName || null,
      nativeDataUrl: opts.nativeDataUrl || null,
      nativeFileName: opts.nativeFileName || null,
      nativeMimeType: opts.nativeMimeType || ctx.mimeType || null,
      workbookFileName: opts.workbookFileName || ctx.fileName || null,
      sheetCount: opts.sheetCount || 1,
      sheetNames: opts.sheetNames || null,
      dimensions: opts.dimensions || null,
      fileSize: opts.fileSize || ctx.fileSize || null,
      nativeExt: opts.nativeExt || null,
      emailMeta: opts.emailMeta || null,
      // Storage reference — same for every page from the same source file.
      // null when running offline or signed-out (local-only mode).
      storagePath: ctx.storagePath || null,
      // Submission linkage (Phase 4). Pulled from state.activeSubmissionId
      // which is set by Altitude via window.docsView.setSubmissionContext
      // when the user opens the docs view from a submission. null when
      // the docs view is opened standalone. Pipeline-driven uploads pass
      // the submission ID via pctx since they may run before the user
      // navigates to the docs view.
      submissionId: opts.submissionId || pctx.submissionId || state.activeSubmissionId || null,
      // Pipeline metadata — populated by classifier hook (Phase 5) or by
      // explicit pushes from Altitude's existing pipeline. null at upload
      // time; the doc may get classified later. Falls through to pctx if
      // processFileFromPipeline was the entry point.
      pipelineClassification: opts.pipelineClassification || pctx.pipelineClassification || null,
      pipelineRoutedTo: opts.pipelineRoutedTo || pctx.pipelineRoutedTo || null,
      // v8.4 fields. pipelineTag is the chip label (e.g., "ACORD 125",
      // "Lead $5M"); primaryBucket is the docs-view category enum from
      // the classifier (CORRESPONDENCE, APPLICATIONS, etc.).
      //
      // FIRST-PAGE-ONLY TAGGING (Rule 2): pipelineTag is set ONLY on
      // page 1 of each section. Pages 2..N inherit the bucket/category
      // (so they live in the same folder) but do NOT get their own chip
      // — that's why the chip rendering in buildDocItem checks for
      // (pageNumber === 1) when falling back to pipelineClassification.
      pipelineTag: ((opts.pageNumber || 1) === 1)
        ? (opts.pipelineTag || pctx.pipelineTag || null)
        : null,
      primaryBucket: opts.primaryBucket || pctx.primaryBucket || null,
      relabeledByUser: !!(opts.relabeledByUser),
      // Color tag — usually null on user upload (user picks via tag menu),
      // but the pipeline-push path (addDocFromPipeline / processFileFromPipeline)
      // sets it from the CATEGORY_MAP so classified docs auto-tag with their
      // category color. Setting a color implies tagged=true so the doc shows
      // up in the Tagged Pages panel filterable by color.
      //
      // FIRST-PAGE-ONLY TAGGING for pipeline ingestion:
      // The pipeline-driven path stamps every page from a multi-page PDF
      // with the same category (so they all live in the right bucket and
      // are findable via the category panel). But we only paint the COLOR
      // TAG on page 1 — the marker for where the doc starts. Pages 2..N
      // share the same submission/category but stay un-tagged so the
      // Tagged Pages count reflects "number of distinct docs", not
      // "number of total pages." Manual uploads (no pctx) keep the
      // legacy behavior — opts.color paints whatever page the caller
      // specifies, since manual users tag pages individually anyway.
      color: opts.color || (pctx.color && (opts.pageNumber || 1) === 1 ? pctx.color : null) || null,
      tagged: !!(opts.color || (pctx.color && (opts.pageNumber || 1) === 1)),
      uploadDate: formatDate(new Date()),
      addedAt: now,
      ocrText: null,
      ocrConfidence: null,
    };
    state.docs.push(doc);
    // Fire-and-forget cloud insert. Local state is the source of truth in
    // the UI; the cloud row is a snapshot/backup. Failures log to console
    // but don't block the upload — user sees their docs immediately.
    //
    // Track the in-flight insert promise so a fast-following delete (user
    // adds and immediately removes a doc) can await it before issuing the
    // delete. Without this, delete-then-insert can arrive out of order at
    // Supabase: delete affects 0 rows (row not yet created), then insert
    // creates the row, leaving an orphan that resurrects on next hydrate.
    if (typeof window.sbInsertDocumentPage === 'function') {
      if (!state._pendingInserts) state._pendingInserts = new Map();
      const insertPromise = window.sbInsertDocumentPage(doc).catch(err => {
        console.warn('Failed to persist doc ' + doc.id + ':', err);
      }).finally(() => {
        if (state._pendingInserts) state._pendingInserts.delete(doc.id);
      });
      state._pendingInserts.set(doc.id, insertPromise);
    }
    return doc;
  }

  // ══════ TAGGING + COLORING ══════
  // Tiny helper that no-ops if Supabase isn't loaded (offline / signed-out
   // / non-Supabase deploys). Every mutation goes through here to keep call
   // sites uncluttered.
  function _persist(docId, patch) {
    if (typeof window.sbUpdateDocumentPage === 'function') {
      window.sbUpdateDocumentPage(docId, patch).catch(err => {
        console.warn('persist patch failed for ' + docId + ':', err);
      });
    }
  }

  function toggleTag(docId) {
    const doc = state.docs.find(d => d.id === docId);
    if (!doc) return;
    doc.tagged = !doc.tagged;
    renderDocsList();
    renderTagsList();
    _persist(docId, { tagged: doc.tagged });
  }

  function setColor(docId, color) {
    const doc = state.docs.find(d => d.id === docId);
    if (!doc) return;
    doc.color = color;
    if (color && !doc.tagged) doc.tagged = true;
    renderDocsList();
    renderTagsList();
    _persist(docId, { color: doc.color, tagged: doc.tagged });
  }

  function clearColor(docId) {
    const doc = state.docs.find(d => d.id === docId);
    if (!doc) return;
    doc.color = null;
    renderDocsList();
    renderTagsList();
    _persist(docId, { color: null });
  }

  function updateTagsCount() {
    // Respect submission scope so the count reflects what's visible.
    const inScope = state.submissionFilter === 'all'
      ? state.docs
      : state.docs.filter(d => d.submissionId === state.submissionFilter);
    const tagged = inScope.filter(d => d.tagged);
    $id('tagsCount').textContent = tagged.length;
    $id('totalTagged').textContent = tagged.length;
  }

  function renderTagsList() {
    const list = $id('tagsList');
    const empty = $id('tagsEmpty');
    // Apply submission scope first so out-of-scope tagged docs don't
    // appear in the sidebar when the user is viewing a single submission.
    const inScope = state.submissionFilter === 'all'
      ? state.docs
      : state.docs.filter(d => d.submissionId === state.submissionFilter);
    const tagged = inScope.filter(d => d.tagged);
    const filtered = state.currentColorFilter === 'all'
      ? tagged
      : tagged.filter(d => d.color === state.currentColorFilter);

    Array.from(list.children).forEach(c => { if (c !== empty) c.remove(); });

    if (filtered.length === 0) {
      empty.style.display = '';
    } else {
      empty.style.display = 'none';
      const colorOrder = [...CONFIG.tagColors, null];
      const sorted = [];
      colorOrder.forEach(c => filtered.forEach(d => { if ((d.color || null) === c) sorted.push(d); }));
      sorted.forEach(doc => list.appendChild(buildTaggedItem(doc)));
    }
    updateTagsCount();
  }

  function buildTaggedItem(doc) {
    const item = document.createElement('div');
    item.className = 'tagged-item' + (doc.color ? ' tag-' + doc.color : '');
    const labelText = doc.color ? CONFIG.tagColorLabels[doc.color] : '';
    item.innerHTML = `
      <div class="tagged-body">
        <div class="tagged-name">${escapeHtml(doc.displayName)}</div>
        ${labelText ? `<span class="tagged-label">${escapeHtml(labelText)}</span>` : ''}
      </div>
      <div class="tagged-actions">
        <button class="doc-mini-btn tagged-remove" title="Remove tag">
          <svg viewBox="0 0 24 24" fill="none" stroke="currentColor" stroke-width="2.2"><line x1="18" y1="6" x2="6" y2="18"/><line x1="6" y1="6" x2="18" y2="18"/></svg>
        </button>
      </div>
    `;
    item.onclick = (e) => {
      if (e.target.closest('.tagged-actions')) return;
      scrollToDoc(doc.id);
    };
    item.querySelector('.tagged-remove').onclick = (e) => {
      e.stopPropagation();
      toggleTag(doc.id);
    };
    return item;
  }

  function scrollToDoc(docId) {
    const doc = state.docs.find(d => d.id === docId);
    if (!doc) return;
    if (state.currentCategory !== 'all' && state.currentCategory !== doc.category) {
      selectCategory('all');
    }
    requestAnimationFrame(() => {
      const el = document.querySelector(`[data-doc-id="${docId}"]`);
      if (!el) return;
      el.scrollIntoView({ behavior: 'smooth', block: 'center' });
      el.style.outline = '2px solid var(--signal)';
      setTimeout(() => { el.style.outline = ''; }, 1500);
    });
  }

  // ══════ RENAME ══════
  function startRename(docId, itemEl) {
    const doc = state.docs.find(d => d.id === docId);
    if (!doc) return;
    const nameEl = itemEl.querySelector('[data-doc-name]');
    const input = document.createElement('input');
    input.className = 'doc-name-input';
    input.type = 'text';
    input.value = doc.displayName;
    nameEl.replaceWith(input);
    input.focus(); input.select();
    const finish = (commit) => {
      let v = input.value.trim();
      if (v.length > 250) v = v.slice(0, 247) + '…';
      if (commit && v && v !== doc.displayName) {
        doc.displayName = v; doc.name = v;
        _persist(docId, { display_name: v });
      }
      renderDocsList(); renderTagsList();
    };
    input.onblur = () => finish(true);
    input.onkeydown = (e) => {
      e.stopPropagation();
      if (e.key === 'Enter') input.blur();
      else if (e.key === 'Escape') finish(false);
    };
  }

