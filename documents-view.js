  async function confirmUpload() {
    if (!state.pendingUpload) return;

    if (state.pendingUpload.bulkMoveIds) {
      const ids = state.pendingUpload.bulkMoveIds;
      const cat = state.pendingUpload.category;
      ids.forEach(id => {
        const d = state.docs.find(x => x.id === id);
        if (d) {
          d.category = cat;
          if (typeof window.sbUpdateDocumentPage === 'function') {
            window.sbUpdateDocumentPage(id, { category: cat }).catch(() => {});
          }
        }
      });
      closeCategoryModal();
      clearSelection();
      renderDocsList();
      toast('Moved', ids.length + ' documents → ' + cat, 'success');
      return;
    }
    if (state.pendingUpload.singleMoveId) {
      const id = state.pendingUpload.singleMoveId;
      const cat = state.pendingUpload.category;
      const doc = state.docs.find(d => d.id === id);
      if (doc) {
        doc.category = cat;
        if (typeof window.sbUpdateDocumentPage === 'function') {
          window.sbUpdateDocumentPage(id, { category: cat }).catch(() => {});
        }
        toast('Moved', doc.displayName + ' → ' + cat, 'success');
      }
      closeCategoryModal();
      renderDocsList();
      return;
    }

    const { files, category } = state.pendingUpload;
    closeCategoryModal();

    // Concurrency lock — chain every batch behind any in-flight one so they
    // run strictly serially. Without serialization, two batches share
    // state._uploadCtx and storage_paths get cross-assigned to the wrong
    // docs.
    //
    // UNIFIED CHAIN: We use state._processFileChain (NOT _uploadChain) so
    // manual batches AND pipeline-driven ingestion (processFileFromPipeline)
    // share the same lock. Both paths set state._uploadCtx and state._pipelineCtx
    // during processFile dispatch — if a manual batch starts while a pipeline
    // ingestion is mid-flight (or vice versa), they'd cross-contaminate. The
    // single chain prevents that. .catch swallowed so a failed earlier batch
    // doesn't poison later ones.
    const prev = state._processFileChain || Promise.resolve();
    state._processFileChain = prev
      .catch(() => {})
      .then(() => _runUploadBatch(files, category));
    return state._processFileChain;
  }

  async function _runUploadBatch(files, category) {
    showLoading('Processing documents');
    let success = 0, errors = 0;
    const errorDetails = [];
    // Reset the storage-fail counter at batch start; processFile bumps it
    // when storage upload returns null while the user is signed in.
    state._batchStorageFails = 0;
    // Track storage paths used by this batch so we can clean orphans at
    // the end. processFile sets state._uploadCtx with the storagePath
    // before dispatching to the type-specific processor; we capture it
    // before each iteration. After all files settle, any storagePath
    // with zero corresponding rows in document_pages is an orphan
    // (entire file failed, OR user cancelled the long-PDF confirm)
    // and gets cleaned up via cleanupOrphanStoragePaths.
    const batchStoragePaths = new Set();

    for (let i = 0; i < files.length; i++) {
      const file = files[i];
      const pct = Math.round((i / files.length) * 100);
      updateLoading(pct, file.name);
      try {
        await processFile(file, category);
        // After processFile returns, state._uploadCtx has been cleared.
        // The storagePath used for this file lives on the local docs that
        // were just added — if any. We capture from state.docs by matching
        // freshness rather than _uploadCtx (which is null by now).
        success++;
      } catch (err) {
        console.error('Failed to process', file.name, err);
        errors++;
        errorDetails.push(file.name + ': ' + (err.message || 'unknown error'));
      }
    }

    updateLoading(100, 'Done');
    await delay(200);
    hideLoading();

    // Wait for any pending Supabase inserts to settle, then clean up
    // orphan storage binaries (files whose row inserts all failed, or
    // whose long-PDF confirm was cancelled by the user). Without this,
    // cancelling a long PDF after the binary uploaded leaves the binary
    // forever in storage with no corresponding row.
    if (state._pendingInserts && state._pendingInserts.size > 0) {
      const pending = Array.from(state._pendingInserts.values());
      if (pending.length > 0) {
        await Promise.allSettled(pending);
      }
    }
    // Collect storage paths from local docs added in this batch. We
    // identify them by scanning state.docs for any with a storagePath
    // we haven't seen before — simpler than tracking a per-file set
    // through the recursive PDF page-split. Even simpler: gather every
    // unique storagePath in state.docs, query the cloud for a count of
    // rows per path, and clean any with zero rows.
    const allStoragePaths = [...new Set(
      state.docs.map(d => d.storagePath).filter(Boolean)
    )];
    if (allStoragePaths.length > 0) {
      cleanupOrphanStoragePaths(allStoragePaths);
    }

    const storageFails = state._batchStorageFails || 0;
    state._batchStorageFails = 0;

    if (errors > 0) {
      const msg = success > 0
        ? success + ' processed, ' + errors + ' failed — ' + errorDetails[0]
        : errorDetails[0];
      toast('Upload issue', msg, success > 0 ? 'warning' : 'error');
    } else if (storageFails > 0) {
      // All processed locally, but some couldn't reach cloud storage. Doc
      // metadata still saved (so tags/annotations work this session) but
      // the binaries won't survive a refresh — user has to re-upload.
      toast(
        'Upload complete · cloud sync incomplete',
        success + ' processed locally; ' + storageFails + ' file' +
          (storageFails !== 1 ? 's' : '') +
          ' couldn\'t reach cloud storage. Check your connection — you may need to re-upload after refresh.',
        'warning'
      );
    } else {
      toast('Upload complete', success + ' file' + (success !== 1 ? 's' : '') + ' ready', 'success');
    }

    if (category !== 'all' && state.currentCategory === 'all') selectCategory(category);
    else renderDocsList();
  }

  // ORPHAN STORAGE CLEANUP HELPER
  // Given a list of candidate storage_path values, query Supabase to find
  // any with zero referencing document_pages rows and remove them from
  // storage. Called at the end of upload batches and pipeline ingestion
  // (the only times new binaries are created).
  //
  // Why batch-level (not per-page-failure): when a multi-page PDF fails
  // mid-stream (page 1 fails, pages 2..99 still in flight), a per-page
  // existence check returns 0 (because 2..99 haven't committed). Deleting
  // the binary at that point would orphan the rows that subsequently
  // commit successfully — the worst failure mode (visible-broken docs).
  // Doing it at batch level, after all pending inserts have settled,
  // gives a definitive "this binary has zero references" answer.
  //
  // Fire-and-forget by design — orphan binaries are cosmetic residue, not
  // a correctness issue. The protect_delete trigger blocks accidents.
  async function cleanupOrphanStoragePaths(storagePaths) {
    if (!Array.isArray(storagePaths) || storagePaths.length === 0) return;
    if (!window.sb) return;
    for (const sp of storagePaths) {
      try {
        const { count, error: chkErr } = await window.sb
          .from('document_pages')
          .select('id', { count: 'exact', head: true })
          .eq('storage_path', sp);
        if (chkErr) continue;
        if (count !== null && count === 0) {
          const { error: rmErr } = await window.sb.storage
            .from('submission-files')
            .remove([sp]);
          if (rmErr) {
            console.warn('Batch orphan cleanup failed for ' + sp + ' (non-fatal):', rmErr.message || rmErr);
          } else {
            console.log('Cleaned orphan binary after failed/cancelled batch:', sp);
          }
        }
      } catch (e) {
        console.warn('Batch orphan check threw for ' + sp + ':', (e && e.message) || e);
      }
    }
  }

  // Per-file size cap. Browser tabs crash beyond ~200MB single-file uploads
  // because the FileReader.readAsDataURL result lives in memory for the
  // lifetime of the doc (used for the "Open in Excel" / native-tile flow).
  // 100MB per file is a generous limit for E&S submissions — broker policies
  // are typically <30MB even with attachments.
  const MAX_FILE_BYTES = 100 * 1024 * 1024;

  // PDFs over this page count get a confirmation prompt before processing,
  // because each page becomes its own doc with thumb (3.0x) + high-res
  // (3.5x) data URLs cached in memory. 500 pages × ~4MB/page = 2GB. The
  // sweet spot for surveys, applications, and policy schedules is well
  // under this; loss runs and big policies sometimes exceed it.
  const PDF_PAGE_WARN_THRESHOLD = 200;

  // ══════ PHASE 5 · LAZY BINARY FETCH ══════
  // Hydrated docs come back from Supabase with their thumbnail intact but
  // the source binary (pdfData / nativeDataUrl) nulled — that data lives
  // in storage and only loads when needed for OCR, full-resolution preview,
  // or download. ensureBinary pulls it on demand via a signed URL.
  // Returns true on success, false otherwise (with a console.warn).
  // Idempotent — calling repeatedly on the same doc only fetches once.
  async function ensureBinary(doc) {
    if (!doc) return false;
    // Already loaded? Done.
    if (doc.pdfData || doc.nativeDataUrl) return true;
    // No storage path means the doc is a hydration without a backing
    // file (legacy data or storage upload failed at upload time).
    if (!doc.storagePath) return false;
    if (typeof window.sbGetDocumentSignedUrl !== 'function') return false;
    // De-duplicate concurrent calls. Two preview opens or an OCR + download
    // in quick succession would otherwise issue two fetches.
    if (doc._binaryFetchPromise) return doc._binaryFetchPromise;
    doc._binaryFetchPromise = (async () => {
      try {
        const url = await window.sbGetDocumentSignedUrl(doc.storagePath, 600);
        if (!url) return false;
        const resp = await fetch(url);
        if (!resp.ok) {
          console.warn('Storage fetch returned ' + resp.status);
          return false;
        }
        const blob = await resp.blob();
        if (doc.type === 'pdf') {
          doc.pdfData = await blob.arrayBuffer();
        } else {
          // For Excel/Word/PowerPoint/email/etc., a blob URL is enough for
          // download + native-tile open; convert to data URL only if the
          // caller explicitly needs base64 (rare).
          doc.nativeDataUrl = URL.createObjectURL(blob);
          if (!doc.nativeMimeType) doc.nativeMimeType = blob.type || 'application/octet-stream';
        }
        return true;
      } catch (err) {
        console.warn('ensureBinary failed for ' + doc.id + ':', err);
        return false;
      } finally {
        // Allow re-fetch on subsequent failure.
        delete doc._binaryFetchPromise;
      }
    })();
    return doc._binaryFetchPromise;
  }

  async function processFile(file, category) {
    // Size sanity check applies to every type uniformly.
    if (file.size > MAX_FILE_BYTES) {
      throw new Error(
        'file too large: ' +
        (file.size / 1024 / 1024).toFixed(1) + ' MB exceeds ' +
        (MAX_FILE_BYTES / 1024 / 1024) + ' MB limit'
      );
    }
    // Upload the original binary to Supabase storage once, BEFORE the
    // type-specific processor runs. Multiple addDoc() calls from one source
    // (PDF page-split, Word page-split, PowerPoint slide-split) all share
    // this same storage_path — they're slices of the same file. addDoc()
    // pulls from state._uploadCtx and persists each row to document_pages.
    // If storage isn't available (signed out, offline, no Supabase) the
    // upload silently no-ops and the view continues in local-only mode.
    const fileId = 'src-' + Date.now() + '-' + Math.random().toString(36).slice(2, 8);
    let storagePath = null;
    if (typeof window.sbUploadDocumentFile === 'function') {
      try { storagePath = await window.sbUploadDocumentFile(file, fileId); }
      catch (e) { console.warn('Storage upload failed for ' + file.name + ':', e); }
    }
    // If storage was supposed to work (we have a Supabase client and a
    // signed-in user) but the upload returned null, count this as a soft
    // failure so we can surface it in the post-batch toast. Doc still gets
    // added to local state so the user can keep working — but they'll see
    // a warning that the binary isn't recoverable across devices/refreshes.
    if (!storagePath && typeof window.sbUploadDocumentFile === 'function') {
      // Only count as failure if a signed-in user exists; otherwise it's
      // expected (offline / signed-out → local-only mode).
      try {
        const u = window.sb ? await window.sb.auth.getUser() : null;
        if (u && u.data && u.data.user) state._batchStorageFails = (state._batchStorageFails || 0) + 1;
      } catch(e) {}
    }
    state._uploadCtx = {
      storagePath,
      fileId,
      fileSize: file.size,
      fileName: file.name,
      mimeType: file.type || 'application/octet-stream',
    };
    try {
      const name = file.name.toLowerCase();
      if (name.endsWith('.pdf')) return await processPDF(file, category);
      if (/\.(xlsx|xlsm|xls|xltx|xltm|xlsb)$/.test(name)) return await processExcel(file, category);
      if (/\.(docx|doc)$/.test(name)) return await processWord(file, category);
      if (name.endsWith('.rtf')) return await processRtf(file, category);
      if (/\.(jpe?g|png|tiff?|gif|webp|bmp)$/i.test(name)) return await processImage(file, category);
      if (/\.(eml|msg|oft)$/.test(name)) return await processEmail(file, category);
      if (/\.(pptx|ppt|ppsx|potx|potm|pptm)$/.test(name)) return await processPowerPoint(file, category);
      if (/\.(csv|tsv)$/.test(name)) return await processCsv(file, category);
      if (/\.(txt|log|md)$/.test(name)) return await processText(file, category);
      if (/\.(zip|rar|7z|tar|gz)$/.test(name)) return await processArchive(file, category);
      return await processNativeOnly(file, category);
    } finally {
      state._uploadCtx = null;
    }
  }

  async function processRtf(file, category) {
    const nativeDataUrl = await cacheNativeFile(file);
    let raw = '';
    try {
      raw = await file.text();
    } catch (e) {
      throw new Error('Could not read RTF: ' + e.message);
    }
    const plain = raw
      .replace(/\\'([0-9a-fA-F]{2})/g, (_, h) => String.fromCharCode(parseInt(h, 16)))
      .replace(/\\u-?\d+\??/g, '')
      .replace(/\\par[d]?\b/g, '\n')
      .replace(/\\tab\b/g, '\t')
      .replace(/\\[a-zA-Z]+-?\d*\s?/g, '')
      .replace(/[{}]/g, '')
      .replace(/\\\\/g, '\\')
      .replace(/\\([{}])/g, '$1')
      .trim();

    const base = stripExt(file.name);
    const LINES_PER_PAGE = 55;
    const lines = plain.split(/\r?\n/);

    if (lines.length <= LINES_PER_PAGE) {
      addDoc({
        name: base,
        type: 'text',
        category,
        htmlContent: '<pre>' + escapeHtml(plain) + '</pre>',
        textContent: plain,
        nativeDataUrl,
        nativeFileName: file.name,
        nativeMimeType: file.type || 'application/rtf',
        workbookFileName: file.name,
        fileSize: file.size,
        nativeExt: 'rtf',
      });
      return;
    }
    const totalPages = Math.ceil(lines.length / LINES_PER_PAGE);
    for (let i = 0; i < totalPages; i++) {
      const chunk = lines.slice(i * LINES_PER_PAGE, (i + 1) * LINES_PER_PAGE).join('\n');
      addDoc({
        name: `${base} — Page ${i + 1}`,
        type: 'text',
        category,
        htmlContent: '<pre>' + escapeHtml(chunk) + '</pre>',
        textContent: chunk,
        pageNumber: i + 1,
        totalPages,
        nativeDataUrl: i === 0 ? nativeDataUrl : null,
        nativeFileName: file.name,
        nativeMimeType: file.type || 'application/rtf',
        workbookFileName: file.name,
        fileSize: file.size,
        nativeExt: 'rtf',
      });
      if (i < totalPages - 1 && i % 10 === 9) await delay(5);
    }
  }

  async function cacheNativeFile(file) {
    const MAX_NATIVE_BYTES = 40 * 1024 * 1024;
    if (file.size > MAX_NATIVE_BYTES) {
      console.warn(file.name + ' is ' + (file.size/1024/1024).toFixed(1) + ' MB — skipping native cache');
      return null;
    }
    try {
      return await new Promise((resolve, reject) => {
        const reader = new FileReader();
        reader.onload = () => resolve(reader.result);
        reader.onerror = () => reject(reader.error || new Error('FileReader failed'));
        reader.readAsDataURL(file);
      });
    } catch (err) {
      console.warn('Could not cache native bytes for', file.name, err);
      return null;
    }
  }

