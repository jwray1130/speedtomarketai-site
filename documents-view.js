  // Free any blob: URLs attached to docs about to be removed. ensureBinary
  // creates these via URL.createObjectURL and they're held alive until
  // explicitly revoked. Without this cleanup, every lazy-fetched native
  // file accumulates in browser memory until tab close.
  // ALSO: clears the annotation store entries and any pending persist
  // timers for the deleted docs. Without this, a doc deleted within the
  // 1-second annotation debounce window would leak its layer state in
  // memory and fire a wasted UPDATE against a deleted row.
  function revokeDocBlobUrls(docs) {
    if (!docs) return;
    docs.forEach(d => {
      if (!d) return;
      // Free blob URL.
      if (typeof d.nativeDataUrl === 'string' && d.nativeDataUrl.startsWith('blob:')) {
        try { URL.revokeObjectURL(d.nativeDataUrl); } catch(e) {}
      }
      // Drop annotation store entry. The annotation engine's debounce
      // map (`_annoPersistTimers`) is private to startAnnoEngine — we
      // expose its cancel via a window hook.
      if (d.id && state.annotations && state.annotations.store) {
        delete state.annotations.store[d.id];
      }
      if (d.id && typeof window.__docsAnno === 'object' && typeof window.__docsAnno.cancelPersist === 'function') {
        try { window.__docsAnno.cancelPersist(d.id); } catch(e) {}
      }
    });
  }

  function updateBulkBar() {
    const bar = $id('bulkBar');
    $id('bulkCount').textContent = state.selectedIds.size;
    bar.classList.toggle('visible', state.selectedIds.size > 0);
  }

  function bulkAction(act) {
    const ids = Array.from(state.selectedIds);
    if (ids.length === 0) return;
    if (act === 'tag') {
      ids.forEach(id => {
        const d = state.docs.find(x => x.id === id);
        if (d) { d.tagged = true; _persist(id, { tagged: true }); }
      });
      toast('Tagged', ids.length + ' documents', 'success');
    } else if (act.startsWith('color-')) {
      const color = act.replace('color-', '');
      ids.forEach(id => {
        const d = state.docs.find(x => x.id === id);
        if (d) {
          d.color = color;
          if (!d.tagged) d.tagged = true;
          _persist(id, { color, tagged: true });
        }
      });
      toast('Colored', ids.length + ' · ' + CONFIG.tagColorLabels[color], 'success');
    } else if (act === 'delete') {
      if (!confirm('Delete ' + ids.length + ' documents?')) return;
      // Capture storage paths before removing from local state so we can
      // delete the binaries too. Also revoke any blob: URLs to free memory.
      const targets = state.docs.filter(d => state.selectedIds.has(d.id));
      revokeDocBlobUrls(targets);
      state.docs = state.docs.filter(d => !state.selectedIds.has(d.id));
      if (typeof window.sbDeleteDocumentPage === 'function') {
        targets.forEach(d => {
          // If an insert for this id is still in flight, wait for it to
          // settle before deleting — otherwise delete may run against a
          // non-existent row, then the insert lands and the doc
          // resurrects on next hydrate.
          const pending = state._pendingInserts && state._pendingInserts.get(d.id);
          const issue = () => window.sbDeleteDocumentPage(d.id, d.storagePath).catch(err => {
            console.warn('Delete failed for ' + d.id + ':', err);
          });
          if (pending) pending.then(issue, issue);
          else issue();
        });
      }
      toast('Deleted', ids.length + ' documents', 'success');
    } else if (act === 'move') {
      state.pendingUpload = { bulkMoveIds: ids, category: 'all' };
      $id('modalSub').textContent = 'Move ' + ids.length + ' document' + (ids.length > 1 ? 's' : '') + ' to:';
      $id('modalConfirmLabel').textContent = 'Move';
      renderModalCategoryGrid();
      const modal = $id('categoryModal');
      modal.classList.add('visible');
      trapFocus(modal);
      return;
    } else if (act === 'download') {
      // Async — return early so the synchronous tail (clearSelection,
      // renderDocsList) doesn't run until the zip completes. The download
      // function clears the selection on success.
      bulkDownloadZip(ids);
      return;
    }
    clearSelection();
    renderDocsList();
    renderTagsList();
  }

  // ══════ PHASE 5 · BULK DOWNLOAD AS ZIP ══════
  // Pulls the source binary for every selected doc (lazy-fetching from
  // storage if needed), packages them into a single .zip with their
  // original filenames, and triggers a browser download. Multi-page PDFs
  // and Word docs share one source binary across pages — the same
  // storage_path produces one entry in the zip, not one per page.
  // For docs with no source binary (text-only entries hydrated long ago,
  // or pre-storage uploads), we synthesize a .txt or .pdf from the cached
  // text/thumbnail so nothing is silently dropped.
  async function bulkDownloadZip(ids) {
    if (typeof JSZip === 'undefined') {
      toast('Download failed', 'JSZip library is not loaded', 'error');
      return;
    }
    showLoading('Building ZIP archive');
    try {
      const zip = new JSZip();
      const docs = state.docs.filter(d => ids.includes(d.id));
      // Group by storage_path so we only fetch each binary once.
      const seenPaths = new Set();
      const filenameCounts = {};
      let added = 0;

      for (let i = 0; i < docs.length; i++) {
        const doc = docs[i];
        updateLoading(Math.round(((i + 1) / docs.length) * 100), doc.displayName);

        // Each unique storage_path contributes one file. Pages 2+ of a
        // multi-page PDF reference the same path and skip.
        if (doc.storagePath && seenPaths.has(doc.storagePath)) continue;

        let blob = null;
        let filename = doc.workbookFileName || doc.nativeFileName ||
                       (doc.displayName + (doc.nativeExt ? '.' + doc.nativeExt : ''));

        if (doc.storagePath) {
          // Try to fetch the original binary.
          if (typeof window.sbGetDocumentSignedUrl === 'function') {
            try {
              const url = await window.sbGetDocumentSignedUrl(doc.storagePath, 600);
              if (url) {
                const resp = await fetch(url);
                if (resp.ok) blob = await resp.blob();
              }
            } catch (err) { console.warn('Storage fetch failed for ' + doc.id + ':', err); }
          }
          seenPaths.add(doc.storagePath);
        }

        // Fallback: if we couldn't get the original, synthesize from cached data.
        if (!blob && doc.nativeDataUrl) {
          blob = await (await fetch(doc.nativeDataUrl)).blob();
        }
        if (!blob && doc.thumbnailData && (doc.type === 'pdf' || doc.type === 'image')) {
          // PNG fallback for image-like docs hydrated without a source.
          blob = await (await fetch(doc.thumbnailData)).blob();
          if (!filename.match(/\.png$/i)) filename = doc.displayName + '.png';
        }
        if (!blob && doc.textContent) {
          blob = new Blob([doc.textContent], { type: 'text/plain' });
          if (!filename.match(/\.(txt|md|csv|tsv|log)$/i)) filename = doc.displayName + '.txt';
        }
        if (!blob) {
          console.warn('Skipped doc ' + doc.id + ' (no recoverable binary)');
          continue;
        }

        // Resolve duplicate filenames across selected docs by suffixing.
        let safeName = filename;
        if (filenameCounts[filename]) {
          filenameCounts[filename]++;
          const dot = filename.lastIndexOf('.');
          const stem = dot > 0 ? filename.slice(0, dot) : filename;
          const ext  = dot > 0 ? filename.slice(dot) : '';
          safeName = stem + ' (' + filenameCounts[filename] + ')' + ext;
        } else {
          filenameCounts[filename] = 1;
        }
        zip.file(safeName, blob);
        added++;
      }

      if (added === 0) {
        toast('Nothing to download', 'No source files were available', 'warning');
        return;
      }

      const zipBlob = await zip.generateAsync(
        { type: 'blob' },
        (meta) => updateLoading(Math.round(meta.percent), 'Compressing…')
      );
      const url = URL.createObjectURL(zipBlob);
      const a = document.createElement('a');
      a.href = url;
      const ts = new Date().toISOString().slice(0, 10);
      a.download = 'documents-' + ts + '.zip';
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      setTimeout(() => URL.revokeObjectURL(url), 60_000);

      toast('Downloaded', added + ' file' + (added !== 1 ? 's' : '') + ' as ZIP', 'success');
      clearSelection();
      renderDocsList();
      renderTagsList();
    } catch (err) {
      console.error('bulkDownloadZip error:', err);
      toast('Download failed', err.message || 'Unknown error', 'error');
    } finally {
      hideLoading();
    }
  }

  // ══════ CONTEXT MENU ══════
  function showContextMenu(x, y) {
    const menu = $id('contextMenu');
    menu.classList.add('visible');
    const rect = menu.getBoundingClientRect();
    const w = rect.width || 220;
    const h = rect.height || 380;
    if (x + w > window.innerWidth) x = window.innerWidth - w - 8;
    if (y + h > window.innerHeight) y = window.innerHeight - h - 8;
    menu.style.left = x + 'px';
    menu.style.top = y + 'px';
  }
  function hideContextMenu() {
    $id('contextMenu').classList.remove('visible');
    state.contextDoc = null;
  }
  function contextAction(act) {
    const id = state.contextDoc;
    hideContextMenu();
    if (!id) return;
    if (act === 'preview') openPreview(id);
    else if (act === 'tag') toggleTag(id);
    else if (act === 'rename') {
      const item = document.querySelector(`[data-doc-id="${id}"]`);
      if (item) startRename(id, item);
    }
    else if (act === 'download') {
      openPreview(id);
      setTimeout(() => downloadPreview(), 300);
    }
    else if (act === 'move') {
      const doc = state.docs.find(d => d.id === id);
      if (!doc) return;
      state.pendingUpload = { singleMoveId: id, category: doc.category };
      $id('modalSub').textContent = 'Move "' + doc.displayName + '" to:';
      $id('modalConfirmLabel').textContent = 'Move';
      renderModalCategoryGrid();
      const modal = $id('categoryModal');
      modal.classList.add('visible');
      trapFocus(modal);
    }
    else if (act === 'delete') {
      const doc = state.docs.find(d => d.id === id);
      if (doc && confirm('Delete "' + doc.displayName + '"?')) {
        const storagePath = doc.storagePath;
        revokeDocBlobUrls([doc]);
        state.docs = state.docs.filter(d => d.id !== id);
        renderDocsList(); renderTagsList();
        if (typeof window.sbDeleteDocumentPage === 'function') {
          const pending = state._pendingInserts && state._pendingInserts.get(id);
          const issue = () => window.sbDeleteDocumentPage(id, storagePath).catch(err => {
            console.warn('Delete failed for ' + id + ':', err);
          });
          if (pending) pending.then(issue, issue);
          else issue();
        }
        toast('Deleted', doc.displayName, 'success');
      }
    }
    else if (act.startsWith('color-')) {
      const color = act.replace('color-', '');
      if (color === 'clear') clearColor(id);
      else setColor(id, color);
    }
  }

